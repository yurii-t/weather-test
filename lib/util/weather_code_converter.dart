class WeatherCodeConverter {
  static String convert(int code) => switch (code) {
        0 => '☀️',
        (1 || 2 || 3) => '🌥️',
        (45 || 48) => '🌫️',
        (51 || 53 || 55 || 56 || 57) => '☁️',
        (61 || 63 || 65) => '🌧️',
        (71 || 73 || 75 || 77) => '❄️',
        (80 || 81 || 82) => '🌧️',
        (95 || 96 || 99) => '⚡',
        (85 || 86) => '🌨️',
        _ => '☀️',
      };
}
